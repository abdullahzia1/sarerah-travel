export function getPackageBySlug(slug: string): Package | undefined {
  return packages.find((p) => p.slug === slug);
}

export function getPackagesByDestination(destinationSlug: string): Package[] {
  return packages.filter((p) => p.destinationSlug === destinationSlug);
}

export function getPackagesByType(type: string): Package[] {
  return packages.filter((p) => p.type.includes(type as Package["type"][0]));
}

export function getFeaturedPackages(limit = 6): Package[] {
  return [...packages]
    .sort((a, b) => (b.rating ?? 0) - (a.rating ?? 0))
    .slice(0, limit);
}
